
# Operators in Shell Scripting :

# Integer Comparison:

# Equal                  =  -eq / == 
# Not Equal              =  -ne / !=
# Greater than           =  -gt
# Greater than equal to  =  -ge
# Less than              =  -lt
# Less than equal to     =  -le


# String Comparison:

# =       : equal to
# !=      : not equal
# -z      : string is empty
# -n      : string is not empty

